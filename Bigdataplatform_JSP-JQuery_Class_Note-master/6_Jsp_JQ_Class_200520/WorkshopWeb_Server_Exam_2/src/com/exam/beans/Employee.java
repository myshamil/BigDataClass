package com.exam.beans;

public class Employee {
	private int ID;
	private String NAME;
	private String EMAIL;
	
	public int getId() {
		return ID;
	}
	public void setId(int id) {
		this.ID = id;
	}
	public String getName() {
		return NAME;
	}
	public void setName(String name) {
		this.NAME = name;
	}
	public String getEmail() {
		return EMAIL;
	}
	public void setEmail(String email) {
		this.EMAIL = email;
	}
	
}